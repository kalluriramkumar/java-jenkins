package calculation;

public class NumberClass {

    public int addNumbers(int arg1, int arg2) {
        return arg1 + arg2;
    }

    public int multiplyNumbers(int arg1, int arg2) {
        return arg1 * arg2;
    }

    public int divideNumbers(int arg1, int arg2) {
        return arg1 / arg2;
    }
}
